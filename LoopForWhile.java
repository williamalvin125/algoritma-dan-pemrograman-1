public class LoopForWhile {

    public static void main(String[] args) {
        
        int sum=0, i=1;
        while (i<20) {
            sum = sum + 1; 
            System.out.println("Sum is " +sum);
            i++;
        }
        System.out.println("Maka, sumnya adalah " + sum);


     int SUM = 0;
     for (int I=1; I<10; I++) {
         SUM=SUM+I;
     }
     System.out.println("sum is "+SUM);
    }
        
    }